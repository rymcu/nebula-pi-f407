// 填充测试数据（0x00~0xFF循环）
void fill_test_pattern(uint8_t* buf) {
    for (uint16_t i = 0; i < 512; i++) {
        buf[i] = i % 256;
    }
}

// 数据校验函数
uint8_t verify_data(uint8_t* buf1, uint8_t* buf2) {
    for (uint16_t i = 0; i < 512; i++) {
        if (buf1[i] != buf2[i]) {
            printf("Data is inconsistent @ offset %d: Written=0x%02X, Read=0x%02X\r\n", i, buf1[i], buf2[i]);
            return 0;
        }
    }
    return 1;
}
